function changeLanguage(lang) {
    localStorage.setItem('kisan_lang', lang);
    applyTranslations(lang);
    
    // Update the selector if present
    const selectors = document.querySelectorAll('.lang-selector');
    selectors.forEach(sel => {
        sel.value = lang;
    });
}

function applyTranslations(lang) {
    const dict = translations[lang] || translations['en'];
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            // If input/textarea, try setting placeholder if it has one
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                // Exceptional cases for specific inputs 
                // Alternatively, we map specific keys for placeholders
                if (key === 'search_ph') el.placeholder = dict[key];
                else el.textContent = dict[key];
            } else {
                el.textContent = dict[key];
            }
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('kisan_lang') || 'en';
    changeLanguage(savedLang);
});
